package array;

public class pairArray1 {

	public static void main(String[] args) {
	

				int [] a= {1,2};
				
				for(int i=0;i<a.length;i++)
				{
					for(int j=0;j<a.length;j++)
					{
						 
						 
						
							 
								System.out.println(a[i]+" "+a[j]);

						 
						 
						
					}
				}
			
	}

}
